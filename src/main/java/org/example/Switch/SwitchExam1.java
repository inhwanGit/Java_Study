package org.example.Switch;

public class SwitchExam1 {
    public static void main(String[] args) {
        int num = 1;
        switch (num) {
            case 1:
                System.out.println("1입니다."); //num이 1일경우
            case 2:
                System.out.println("2입니다."); //num이 2일경우
            case 3:
                System.out.println("3입니다."); //num이 3일경우
        }
    }
}

