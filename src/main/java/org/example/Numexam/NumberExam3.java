package org.example.Numexam;

public class NumberExam3 {
    public static void main(String[] args) {
        int a = 5;
        a += 3;

        int b = 4;
        b -= 2;

        int c = 3;
        c *= 5;

        int d = 4;
        d /= 2;

        int e = 5;
        e %= 5;

        System.out.println(a);
        System.out.println(b);
        System.out.println(c);
        System.out.println(d);
        System.out.println(e);

    }
}
